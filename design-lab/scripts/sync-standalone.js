#!/usr/bin/env node

/**
 * sync-standalone.js
 *
 * Runs before `next dev` and `next build`.
 * 1. Copies standalone-apps/ → design-lab/public/standalone-apps/
 * 2. Generates design-lab/lib/standalone-generated.ts from each folder's metadata.json
 *
 * Both outputs are git-ignored — this script is the source of truth.
 */

const fs = require('fs')
const path = require('path')
const { execSync } = require('child_process')

const REPO_ROOT = path.resolve(__dirname, '..', '..')
const STANDALONE_SRC = path.join(REPO_ROOT, 'standalone-apps')
const PUBLIC_DEST = path.join(__dirname, '..', 'public', 'standalone-apps')
const GENERATED_FILE = path.join(__dirname, '..', 'lib', 'standalone-generated.ts')

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function copyDirSync(src, dest) {
  fs.mkdirSync(dest, { recursive: true })
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const srcPath = path.join(src, entry.name)
    const destPath = path.join(dest, entry.name)
    if (entry.isDirectory()) {
      copyDirSync(srcPath, destPath)
    } else {
      fs.copyFileSync(srcPath, destPath)
    }
  }
}

function getGitTimestamp(folderPath) {
  try {
    const result = execSync(
      `git log -1 --format="%ct" -- "${folderPath}"`,
      { cwd: REPO_ROOT, encoding: 'utf8', stdio: ['pipe', 'pipe', 'ignore'] }
    ).trim()
    return result ? parseInt(result, 10) * 1000 : Date.now()
  } catch {
    return Date.now()
  }
}

function formatRelativeTime(ms) {
  const diff = Date.now() - ms
  const minutes = Math.floor(diff / 60000)
  const hours = Math.floor(diff / 3600000)
  const days = Math.floor(diff / 86400000)
  const weeks = Math.floor(days / 7)
  if (minutes < 60) return `Committed ${minutes <= 1 ? 'just now' : `${minutes} minutes ago`}`
  if (hours < 24) return `Committed ${hours} hour${hours === 1 ? '' : 's'} ago`
  if (days < 7) return `Committed ${days} day${days === 1 ? '' : 's'} ago`
  return `Committed ${weeks} week${weeks === 1 ? '' : 's'} ago`
}

// ---------------------------------------------------------------------------
// Step 1: Copy standalone-apps/ → public/standalone-apps/
// ---------------------------------------------------------------------------

if (!fs.existsSync(STANDALONE_SRC)) {
  console.log('[sync-standalone] No standalone-apps/ folder found — skipping.')
  process.exit(0)
}

// Clear and recreate destination
if (fs.existsSync(PUBLIC_DEST)) {
  fs.rmSync(PUBLIC_DEST, { recursive: true, force: true })
}
fs.mkdirSync(PUBLIC_DEST, { recursive: true })

const entries = fs.readdirSync(STANDALONE_SRC, { withFileTypes: true })
  .filter(e => e.isDirectory() && !e.name.startsWith('_') && !e.name.startsWith('.'))

for (const entry of entries) {
  const src = path.join(STANDALONE_SRC, entry.name)
  const dest = path.join(PUBLIC_DEST, entry.name)
  copyDirSync(src, dest)
  console.log(`[sync-standalone] Copied ${entry.name}`)
}

// ---------------------------------------------------------------------------
// Step 2: Generate lib/standalone-generated.ts
// ---------------------------------------------------------------------------

const projects = []

for (const entry of entries) {
  const metaPath = path.join(STANDALONE_SRC, entry.name, 'metadata.json')
  if (!fs.existsSync(metaPath)) {
    console.warn(`[sync-standalone] Skipping ${entry.name} — no metadata.json found`)
    continue
  }

  let meta
  try {
    meta = JSON.parse(fs.readFileSync(metaPath, 'utf8'))
  } catch (e) {
    console.warn(`[sync-standalone] Skipping ${entry.name} — invalid metadata.json`)
    continue
  }

  const committedAt = getGitTimestamp(path.join(STANDALONE_SRC, entry.name))
  const committed = formatRelativeTime(committedAt)

  projects.push({
    slug: entry.name,
    name: meta.name,
    href: `/standalone-apps/${entry.name}/`,
    team: meta.team,
    description: meta.description,
    committedAt,
    committed,
    decoration: meta.decoration,
    type: 'standalone',
    author: meta.author,
  })
}

const fileContent = `// AUTO-GENERATED by scripts/sync-standalone.js — do not edit manually
// Re-generated on every \`npm run dev\` and \`npm run build\`

import { Project } from './folders'

export type StandaloneProject = Project & { type: 'standalone'; author: string }

export const STANDALONE_PROJECTS: StandaloneProject[] = ${JSON.stringify(projects, null, 2)}
`

fs.writeFileSync(GENERATED_FILE, fileContent, 'utf8')
console.log(`[sync-standalone] Generated standalone-generated.ts with ${projects.length} project(s)`)
